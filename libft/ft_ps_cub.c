#include "libft.h"

int	ps_cub(char *s, int fd, int err)
{
	int	i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			ft_putchar_fd(s[i], fd);
			i++;
		}
	}
	return (err);
}
